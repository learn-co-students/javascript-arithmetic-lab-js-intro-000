function add(a,b){
  return a + b
}

function subtract(b,a){
  return b - a
}

function multiply(a,b){
  return a*b
}

function divide(a,b){
  return a/b
}

function inc(n){
  var increment = ++n
  return increment
}

function dec(n){
  var decrement = --n
  return decrement
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}
