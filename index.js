function add(a, b){
  var sum = a + b;
  return sum;
}
function subtract (a, b){
  var sub = a - b;
  return sub;
}
function multiply(a, b){
  var mul = a * b;
  return mul;

}
function divide (a , b){
  var div = a / b;
  return div;
}
function inc(n){
  var increment = n + 1;
  return increment;
}
function dec(n){
  var decrement = n - 1;
  return decrement;
}
function makeInt(n){
  var par = parseInt(n, 10);
  return par;
}
function preserveDecimal(n){
  var pre = parseFloat(n);
  return pre;
}
