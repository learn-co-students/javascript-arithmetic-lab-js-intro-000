function add(x, y) {
  return  x + y
}
function subtract(x, y) {
  return x - y
}
function divide(x, y) {
  return x/y
}

function multiply(x, y)  {

  return x * y
}


function increment(number) {
  number ++
  return number
}

function decrement(number){
  number --
  return number
}

function makeInt(string) {
return  parseInt(string, 10)
}

function preserveDecimal(string) {
  return parseFloat(string)
}
