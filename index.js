function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
  parseInt(n + 1);
  return n + 1;
}

function dec(n) {
  parseFloat(n - 1);
  return n - 1;
}

function makeInt(n) {
  parseInt(n);
  return n;
}

