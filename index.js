function add(num, num2) {
  return num+num2;
}

function subtract(num, num2) {
  return num-num2;
}

function multiply(num, num2) {
  return num*num2;
}

function divide(num, num2) {
  return num/num2;
}

function inc(num) {
  return num+=1;
}

function dec(num) {
  return num-=1;
}

function makeInt(num) {
  return parseInt(num, 10);
}

function preserveDecimal(num) {
  return parseFloat(num);
}
