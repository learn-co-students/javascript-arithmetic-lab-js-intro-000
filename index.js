function add(a, b) {
return a + b
}

function subtract(a, b) {
return a - b
}

function multiply(a, b) {
return a * b
}

function divide(a, b) {
return a / b
}

function inc(a) {
  return a += 1
}

function dec(a) {
  return a -= 1
}

function makeInt(string, radix) {
  return parseInt(string, 10)
}

function preserveDecimal(string) {
  return parseFloat(string)
}
