function add(num1, num2) {
  return num1 + num2
}

function subtract(num1, num2) {
  return num1 - num2
}

function multiply(num1, num2) {
  return num1 * num2
}

function divide(num1, num2) {
  return num1/num2
}

function inc(num) {
  var value = num + 1
  return value
}

function dec(num) {
  var value = num -1
  return value
}

function makeInt(n) {
  return parseInt(n,10)
}

function preserveDecimal(n) {
  return parseFloat(n, 10)
}
