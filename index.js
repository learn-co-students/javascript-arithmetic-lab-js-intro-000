function add(a, b){
  return (a+b)
}

function subtract(a,b){
  return (a-b)
}

function multiply(a,b){
  return (a*b)
}

function divide(a,b){
  return (a/b)
}

function inc(n){
  n += 1
  return n
}

function dec(n){
  n -= 1
  return n
}

function makeInt(n){
  var pint = parseInt(n,10)
  return pint
}

function preserveDecimal(n){
  var pfloat = parseFloat(n)
  return pfloat
}