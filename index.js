function add(a,b) {
var ans = a+b
return ans;
}
function subtract(a,b) {
var ans = a-b
return ans;
}
function multiply(a,b) {
var ans = a*b
return ans;
}
function divide(a,b) {
var ans = a/b
return ans;
}

function increment(n) {
var n = n+1
return n;
}
function decrement(n) {
  var n = n-1
  return n;
}
function makeInt(string) {
  var string = parseInt(string, 10)
  return string;
}
function preserveDecimal(string) {
  var string = parseFloat(string)
  return string;
}
