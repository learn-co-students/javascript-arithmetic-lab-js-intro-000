function add(a, b) {
  return (a + b)
}

function subtract(a, b) {
  return (a - b)
}

function divide(a, b) {
  return (a / b)
}
function multiply(a, b) {
  return (a * b)
}

function increment(number) {
  number++
  return number
}

function decrement(number) {
  number--
  return number
}

function makeInt(string) {
  return parseInt(string, 10)
}

function preserveDecimal(string) {
  return parseFloat(string)
}
