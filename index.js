function add(numA, numB) {
  return numA+numB;
}

function subtract(numA, numB) {
  return numA-numB;
}

function multiply(numA, numB) {
  return numA*numB;
}

function divide(numA, numB) {
  return numA/numB;
}

function inc(num) {
  return ++num;
}

function dec(num) {
  return --num;
}

function makeInt(val) {
  return parseInt(val, 10);
}

function preserveDecimal(val) {
  return parseFloat(val);
}