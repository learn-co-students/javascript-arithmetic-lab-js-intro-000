function add(first, second) {
  var total = first + second;
  return total;
  
}
function subtract(first, second) {
  var total = first - second;
  return total;
  
}
function multiply(first, second) {
  var total = first * second;
  return total;
 
}
function divide(a, b) {
  var total = a / b;
  return total;
  
}
function increment(n) {
  var total =   ++n;
  return total;
  
}
function decrement(n) {
  var total = --n;
  return total;
  
}


