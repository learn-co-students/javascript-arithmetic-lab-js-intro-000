function add(a, b){return a+b}

function subtract(a, b){return a-b}

function multiply(a, b){return a*b}

function divide(a, b){return a/b}

function inc(n){
  var i=n; i++;
   return i}

function dec(n){
  var i=n; i--;
   return i}

function makeInt(n){
var parsed = parseInt(n, 10);
return parsed
}

function preserveDecimal(n){
var float = parseFloat(n);
return float
}
