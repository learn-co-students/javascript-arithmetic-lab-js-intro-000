function add(a,b){
  var sum = a+b;
  return sum;

}

function subtract(a,b){
  var difference = a-b;
  return difference;
}

function multiply(a,b){
  var product = a*b;
  return product;

}

function divide(a,b){
  var quotient = a/b;
  return quotient;
}

function inc(n){
  var number = n+=1;
  return number;
}

function dec(n){
  var number = n-=1;
  return number;
}

function makeInt(n){
  var integer = parseInt(n,10);
  return integer
}

function preserveDecimal(n){
  var decimal = parseFloat(n);
  return decimal;
}
