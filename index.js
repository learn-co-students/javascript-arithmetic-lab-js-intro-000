function add(a, b) {
  return a+b;
}

function subtract(a, b) {
  return a-b;
}

function multiply(a, b) {
  return a*b;
}

function divide(a, b) {
  return a/b;
}

function inc(n) {
var n;
n++
return n;
}

function dec(n) {
  var n;
  n--;
  return n;
}

function makeInt(x) {
  var n = parseInt(x, 10);
  return n;
}

function preserveDecimal(x) {
  var n = parseFloat(x);
  return n;
}
