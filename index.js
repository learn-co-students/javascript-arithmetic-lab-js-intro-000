function add(a,b){
  var result = a + b
  return result
}

function subtract(a,b){
  var result = a - b;
  return result
}

function multiply(a,b){
  var result = a * b;
  return result
}

function divide(a,b){
  var result = a/b;
  return result
}

function inc(n){
  var number = n;
  number++
  return number
}

function dec(n){
  var number=n;
  number--;
  return number
}

function makeInt(n){
  var solution = parseInt(n,10)
  return solution
}

function preserveDecimal(n){
  var solution = parseFloat(n)
  return solution
}
