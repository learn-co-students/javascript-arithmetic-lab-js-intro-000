function add(a, b) {
  return (a + b)
}

function subtract(a, b) {
  return (a - b)
}

function multiply(a, b) {
  return (a * b)
}

function divide(a, b) {
  return (a / b)
}

function makeInt(n) {
  var a = parseInt(n, 10);
  return a;
}

function preserveDecimal(n) {
  var a = parseFloat(n);
  return a;
}

function inc(n) {
  return (n + 1)
}

function dec(n) {
  return (n - 1)
}
