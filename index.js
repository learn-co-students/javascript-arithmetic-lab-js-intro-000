function add(number1,number2){
  return number1 + number2
}

function subtract(a,b){
  return a-b
}

function multiply(a,b){
  return a*b
}

function divide(a,b){
  return a/b
}

function increment(n){
  return n+=1
}

function decrement(n){
  return n-=1
}

// makeInt()
function makeInt(a){
  return parseInt(a,10)
}

function preserveDecimal(a){
  return parseFloat(a)
}
