function add(a, b) {
  var total = a + b;
  return total
}
function subtract(a, b) {
  var total = a - b;
  return total
}
function multiply(a, b) {
  var total = a * b;
  return total
}
function divide(a, b) {
  var total = a / b;
  return total
}
function inc(n) {
  var total = n += 1
  return total
}
function dec(n) {
  var total = n -= 1
  return total
}
function makeInt(n) {
  var total = parseInt(n, 10)
  return total
}
function preserveDecimal(n) {
  var total = parseFloat(n, 10)
  return total
}
