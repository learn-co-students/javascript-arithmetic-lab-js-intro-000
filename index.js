function add(a, b) {
  var answer = a + b;
  return answer
}

function subtract(a, b) {
  var answer = a - b;
  return answer
}

function multiply(a, b) {
  var answer = a * b
  return answer
}

function divide(a, b) {
  var answer = a / b
  return answer
}

function inc(a) {
  var answer = a
  answer++
  return answer
}

function dec(a) {
  var answer = a
  answer--
  return answer
 }

function makeInt(a) {
  return parseInt(a, 10)
}

function preserveDecimal(a) {
  return parseFloat(a)
}
