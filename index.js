//Arithmetic operations in JavaScript

//Variable declarations
var a = 10
var b = 5
var n = 100

//Functions
function add(a, b) {
  return (a + b)
}

function subtract(a, b) {
  return (a - b)
}

function multiply(a, b) {
  return (a * b)
}

function divide(a, b) {
  return (a / b)
}

function inc(n) {
  n++
  return (n)
}

function dec(n) {
  n--
  return (n)
}

function makeInt(n) {
  return (parseInt(n, 10))
}

function preserveDecimal(n) {
  return (parseFloat(n))
}
