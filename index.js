function add(a, b) {
  return a + b
}

function subtract(b, a) {
  return b - a
}

function multiply(a, b) {
  return a * b
}

function divide (a, b) {
  return a / b
}

function inc(n) {
  parseInt(n)
  return n += 1
}

function dec(n) {
  return n -= 1
}

function makeInt(n) {
  return parseInt(n, 10)
  return n *= 0
}

function preserveDecimal(n) {
  parseFloat(n)
  return n
}
