function add(a, b)
  {
    return a + b
  }

function subtract(a, b)
  {
    return a - b
  }

function multiply(a,b)
  {
  return a * b
  }

function divide(a,b)
{
  return a / b
}

function inc(a)
{
  a++
  return a
}

function dec(a)
{
  a--
  return a
}

function makeInt(n)
{
  var K = parseInt(n, 10)
  return K
}

function preserveDecimal(n)
{
  var J = parseFloat(n)
  return J
}
