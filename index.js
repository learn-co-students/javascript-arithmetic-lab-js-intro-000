function add (a,b)
{return a+b}

function subtract(a,b)
{return a-b}

function multiply(a,b)
{return a*b}

function divide (a,b)
{return a/b}

function increment(n)
{return n+1}

function decrement(n)
{return n-1}

function makeInt (n)
{ var n = n.toString()
return parseInt(n,10)
}

function preserveDecimal(n)
{ var n = n.toString()
return parseFloat(n,10)
}
