function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
  return n+1;
}

function dec(n) {
  return n-1;
}

function makeInt(i) {
  parseInt(i, 10)
  return parseInt(i, 10);
}

function preserveDecimal(f) {
  parseFloat(f, 10)
  return parseFloat(f, 10);
}
