function add(a, b) {
  var plus = a + b
  return plus
}

function subtract(a, b) {
  var minus = a - b
  return minus
}

function multiply(a, b) {
  var times = a * b
  return times
}

function divide(a, b) {
  var into = a / b
  return into
}

function inc(n) {
  n++
 return n
}

function dec(n) {
  n--
 return n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
