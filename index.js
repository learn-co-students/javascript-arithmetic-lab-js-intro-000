function add(a,b){
  return a+b
}

function subtract(a,b){
  return a-b
}

function multiply(a,b){
  return a*b
}

function divide(a,b){
  return a/b
}

function inc(n){
  return ++n
}

function dec(n){
  return --n
}

var n=n.toString()
function makeInt(n){
  return parseInt(n,10)
}

var n=n.toString()
function preserveDecimal(n){
  return parseFloat(n)
}
