function add(a,b){
  return a + b
}
function subtract(b,a){
  return b - a
}
function multiply(a,b){
  return a * b
}
function divide(a,b){
  return a / b
}
function increment(n){
  return ++n
}
function decrement(n){
  return --n
}

function makeInt(n){
  return parseInt(n,10)
}
function preserveDecimal(n){
  return parseFloat(n)
}
