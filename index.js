function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
 n++;
 return n;
}

function dec(n) {
 n--;
 return n;
}

function makeInt(n) {
  var i = parseInt(n, 10);
  return i;
}

function preserveDecimal(n) {
  var f = parseFloat(n);
  return f;
}
