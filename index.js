let add = function(a, b) {
  return a + b;
}

let subtract = function(a,b) {
  return a - b;
}

let multiply = function(a,b) {
  return a * b;
}

let divide = function(a, b) {
  return a/b;
}

let inc = function(n) {
  return n += 1;
}

let dec = function(n) {
  return n -= 1;
}

let makeInt = function(a){
  return parseInt(a, 10);
}

let preserveDecimal = function(a) {
  return parseFloat(a);
}
