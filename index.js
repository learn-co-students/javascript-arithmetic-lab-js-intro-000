function add(a,b){
  var sum = a+b;
  return sum;
}

function subtract(a,b){
  return a-b;
}

function multiply(a,b){
  return a*b;
}

function divide(a,b){
  return a/b;
}

function increment(n){
  n = n+1;
  return n;
}

function decrement(n){
  n = n-1;
  return n;
}

function makeInt(n){
  return parseInt(n, 10);
}

function preserveDecimal(n){
  return parseFloat(n);
}
