function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function increment(n) {
  return ++n
}

function decrement(n) {
  return --n
}

function makeInt(s) {
  return parseInt(s, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
