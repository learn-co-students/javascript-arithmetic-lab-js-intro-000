const add = (a, b) => {
    return a + b
}

function subtract(a, b) {
    return a - b
}

const multiply = (a, b) => {
    return a * b
}

const divide = (a, b) => a/b

const increment = function(n) {
    n++
    return n
}

function decrement(n) {
    n-= 1
    return n
}

function makeInt(n) {
   return parseInt(n, 10)
    
}

function preserveDecimal(n) {
   return parseFloat(n)
}

