function add(a, b) {
  var c = a+b;
  return c;
}

function subtract(a, b) {
  var c = a-b;
  return c;
}

function multiply(a, b) {
  var c = a*b;
  return c;
}

function divide(a, b) {
  var d = a/b;
  return d;
}

function inc(n) {
  var c = n+1;
  return c;
}

function dec(n) {
  var c = n-1;
  return c;
}

function makeInt(n) {
  var c = parseInt(n, 10);
  return c;
}

function preserveDecimal(n) {
  var c = parseFloat(n);
  return c;
}