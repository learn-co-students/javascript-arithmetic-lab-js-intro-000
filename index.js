function add(a, b) {
  var x;
  x = a + b;
  return x;
}

function subtract(a, b) {
  var x;
  x = a - b;
  return x;
}

function multiply(a, b) {
  var x;
  x = a * b;
  return x;
}

function divide(a, b) {
  var x;
  x = a / b;
  return x;
}

function inc(n) {
  var n;
  n++;
  return n;
}

function dec(n) {
  var n;
  n--;
  return n;
}

