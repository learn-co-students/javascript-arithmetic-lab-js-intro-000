function add(a,b){
  a + b;
  var result = a + b;
  return result
}

function subtract(a,b){
  a - b;
  var result = a - b;
  return result
}

function multiply(a,b){
  a * b;
  var result = a * b;
  return result
}

function divide(a,b){
  a / b;
  var result = a / b;
  return result
}

function inc(n){
  n++
  var result = n++
  return result
}

function dec(n){
  n--
  var result = n--
  return result
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n);
}
