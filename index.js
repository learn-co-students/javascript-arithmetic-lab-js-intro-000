function add(a,b) {
  var sum = a+b;
  return sum;
}

function subtract (a,b) {
  var difference = a - b;
  return difference;
}

function multiply(a,b) {
  var product = a * b;
  return product;
}

function divide(a,b) {
  var division = a/b;
  return division;
}

function inc(n) {
  n++;
  return n;
}

function dec(n) {
  n--;
  return n;
}

function makeInt(n) {
var result = parseInt(n, 10);
return result;
}

function preserveDecimal(n) {
  var result = parseFloat(n);
  return result;
}
