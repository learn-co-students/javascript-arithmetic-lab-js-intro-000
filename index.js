function add(a, b){
  return a + b;
}

function subtract(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function inc(n){
  var answer = n++
  return answer;
}

function dec(n){
  var answer = n--
  return answer;
}

function makeInt(n){
  var answer = parseInt(n, 10)
  return answer;
}

function preserveDecimal(n){
  var answer = parseFloat(n)
  return answer;
}
