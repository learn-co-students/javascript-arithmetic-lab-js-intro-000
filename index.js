function add(x,y){
  return x+y;
}
function subtract(x,y){
  return x-y;
}
function multiply(x,y){
  return x*y;
}
function divide(x,y){
  return x/y;
}
function inc(x){
  x+=1;
  return x;
}
function dec(x){
  return x-=1;
}
function makeInt(s){
  return parseInt(s,10);
}
function preserveDecimal(n){
return parseFloat(n);
}
