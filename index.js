function add(a,b){
  var num = a + b;
  return num;
}

function subtract(a,b){
  var num = a - b;
  return num;
}

function multiply(a,b){
  var num = a * b;
  return num;
}

function divide(a, b){
  var num = a / b;
  return num;
}

function inc(n){
  return n+=1
}

function dec(n){
  return n-=1
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}