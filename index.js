function add(a,b) {
  return a + b;
}

function subtract(a,b) {
  return a-b;
}

function multiply(a,b) {
  return a*b;
}

function divide(a,b) {
  return a/b;
}

function increment(n) {
  // n+=1;
  return n+=1;
}

function decrement(n) {
  return n-=1;
}

function makeInt(str) {
  return parseInt(str,10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
