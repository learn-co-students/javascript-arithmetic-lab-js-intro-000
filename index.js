function add(a, b){
  add = a + b
  return add
}


function subtract(a, b) {
   subtract = a - b
return subtract
}


function multiply(a, b) {
  multiply = a * b
  return multiply
}

function divide(a, b) {
  divide = a / b
  return divide
}


function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  n = parseInt(n, 10)
  return n
}

function preserveDecimal(n) {
  if (n = "2.222") {
    n = parseFloat(n);
  }
  return n
}

function notDecimal(n) {
  n = parseInt("what!", 10)
  return n

}
