function makeInt(string)  {
    parseInt(string)
    return parseInt(string, 10)
}
function preserveDecimal(string)  {
    parseFloat(string)
    return parseFloat(string)
}
function add(a, b)  {
    return a + b
}
function subtract(a, b)   {
    return a - b
}
function multiply(a, b) {
    return a * b
}
function divide(a, b) {
    return a / b
}
function increment(number)  {
  return ++number
}
function decrement(number)  {
    return --number
}
