function add(val1,val2){
  return val1+val2;
}

function subtract(val1,val2){
  return val1-val2;
}

function multiply(val1,val2){
  return val1*val2;
}

function divide(val1,val2){
  return val1/val2;
}

function inc(num){
  return ++num;
}

function dec(num){
  return --num;
}

function makeInt(num){
  return parseInt(num,10);
}

function preserveDecimal(num){
  return parseFloat(num);
}
