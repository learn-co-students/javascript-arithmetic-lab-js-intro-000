function add (a, b) {
  return a += b;
}

function subtract (a, b) {
  return a -= b;;
}

function multiply (a, b) {
  return a *= b;
}

function divide (a, b) {
  return a /= b;
}

function increment (n) {
  return ++n;
}

function decrement (n) {
  return --n;
}

function makeInt(a, b) {
  return parseInt(a, 10);
}

function preserveDecimal(a, b) {
  return parseFloat(a, b);
}
