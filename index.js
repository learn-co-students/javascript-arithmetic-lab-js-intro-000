
function add(a, b) {
  add = (a + b);
  return add
}

function subtract(a, b) {
  subtract = (a - b);
  return subtract
}

function multiply(a, b) {
  multiply = (a * b);
  return multiply
}

function divide(a, b) {
  divide = (a / b);
  return divide
}

function increment(n) {
  var increment = n += 1;
  return increment
}

function decrement(n) {
  var decrement = n -= 1;
  return decrement
}

function makeInt(n) {
  var makeInt = parseInt(n, 10);
  return makeInt
}

function preserveDecimal(n) {
  var preserveDecimal = parseFloat(n);
  return preserveDecimal
}
