function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
n++
return n;
}

function dec(n) {
n--
return n;
}

function makeInt(n) {
let int = parseInt(n, 10);
return int;
}

function preserveDecimal(n) {
let psflt =  parseFloat(n);
return psflt;
}
