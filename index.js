function add(a, b){
  return a+b;
}

function subtract(a, b){
  return a-b;
}

function multiply(a, b){
  return a*b;
}

function divide(a,b){
  return a/b;
}

function inc(n){
  return ++n;
}

function dec(a){
  return --a;
}

function makeInt(string){
  return parseInt(string, 10);
}

function preserveDecimal(n){
  return parseFloat(n);
}
