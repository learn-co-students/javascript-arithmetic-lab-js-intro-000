function add(a, b) {
return a + b
}

function subtract(a, b) {
return a - b
}

function multiply(a, b) {
return a * b
}

function divide(a, b) {
return a / b
}

function increment(n) {
return n += 1
}

function decrement(n) {
return n -= 1
}

function makeInt(string) {
  parseInt('2', 10)
  return parseInt(string, 10)
}

function preserveDecimal(a) {
  parseFloat('2.222')
  return parseFloat(a)
}
